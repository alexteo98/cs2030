class Multiply extends Operation<Integer>  { 
  private Integer op1;
  private Integer op2;

  public Multiply(Operand<Integer> op1, Operand<Integer> op2) { 
    this.op1 = op1.eval();
    this.op2 = op2.eval();
  }

  public Integer eval() { 
    return this.op1 * this.op2;
  }
}
